# WAP to enter two number and perform all arithmatic operation 

# Main code to get input from the user and display the results

# Enter two numbers
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Perform arithmetic operations
sum_of_numbers = num1 + num2
difference_of_numbers = num1 - num2
product_of_numbers = num1 * num2
quotient_of_numbers = num1 / num2
modulus_of_numbers = num1 % num2

# Display the results
print(f"The sum of {num1} and {num2} is: {sum_of_numbers}")
print(f"The difference of {num1} and {num2} is: {difference_of_numbers}")
print(f"The product of {num1} and {num2} is: {product_of_numbers}")
print(f"The quotient of {num1} and {num2} is: {quotient_of_numbers}")
print(f"The modulus of {num1} and {num2} is: {modulus_of_numbers}")

